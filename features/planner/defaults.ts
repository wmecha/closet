const today = new Date().toISOString().slice(0, 10);

const expense = (
  id: string,
  name: string,
  category: string,
  amount: number,
  allocateToInventory: boolean,
): PlannerExpense => ({
  id,
  name,
  category,
  amount,
  costType: "fixed",
  allocateToInventory,
  notes: "",
});

const category = (
  id: string,
  name: string,
  quantity: number,
  minBuyingPrice: number,
  averageBuyingPrice: number,
  maxBuyingPrice: number,
  onlinePrice: number,
  minimumSellerReturn: number,
  clearancePrice: number,
  priority: PlannerCategory["priority"],
): PlannerCategory => ({
  id,
  name,
  budget: quantity * averageBuyingPrice,
  quantity,
  minBuyingPrice,
  averageBuyingPrice,
  maxBuyingPrice,
  onlinePrice,
  marketPrice: minimumSellerReturn,
  clearancePrice,
  onlinePercent: 35,
  marketPercent: 60,
  clearancePercent: 5,
  discountPercent: 0,
  damagedPercent: 2,
  unsoldPercent: 10,
  priority,
  notes: "",
});

