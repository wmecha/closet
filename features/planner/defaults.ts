export function createDefaultScenario(
  overrides: Partial<PlannerScenario> = {},
): PlannerScenario {
  const timestamp = new Date().toISOString();
  return {
    id: crypto.randomUUID(),
    name: "First Meru Ladies Drop",
    description:
      "50-piece starter plan with an adjustable KES 100-200 buying range, KES 150 average cost, controlled online pricing and seller minimum returns.",
    status: "draft",
    mode: "quantity",
    totalCapital: 50_000,
    plannedStockBudget: 7_500,
    targetPricingMethod: "markup",
    targetRatePercent: 50,
    expectedSellThroughPercent: 90,
    plannedSourcingDate: today,
    plannedLaunchDate: today,
    notes: "",
    priceRounding: "nearest_50",
    paymentChargePercent: 2,
    sellerCommission: {
      type: "none",
      amount: 0,
      targetAmount: 0,
    },
    expenses: defaultExpenses.map((item) => ({ ...item })),
    categories: defaultCategories.map((item) => ({ ...item })),
    createdAt: timestamp,
    updatedAt: timestamp,
    ...overrides,
  };
}

